/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func deleteDuplicates(head *ListNode) *ListNode {
	pre := &ListNode{Val: 0, Next: head}
	root := pre
	temp := head

	for temp.Next != nil {
		if temp.Val == temp.Next.Val {
			temp = temp.Next
			continue
		}
		pre.Next = temp.Next
		temp = temp.Next
	}
	return root.Next
}
